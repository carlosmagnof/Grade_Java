
package grade;

public class Disciplina {
    double horarioinicial;
    double horariofinal;
    int sala;
    String professor;
    String diadasemana;

    public Disciplina(double horarioinicial, double horariofinal, int sala, String professor, String diadasemana) {
        this.horarioinicial = horarioinicial;
        this.horariofinal = horariofinal;
        this.sala = sala;
        this.professor = professor;
        this.diadasemana = diadasemana;
    }
    
}
